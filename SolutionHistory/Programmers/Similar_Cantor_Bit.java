    public int cal(long i) {
        long n = i - 1;
        while (n > 5) {
            long q = n / 5;
            long r = n % 5;

            if (r == 2L) {
                return 0;
            }

            n = q;
        }
        return (n == 2L) ? 0 : 1;
    }


